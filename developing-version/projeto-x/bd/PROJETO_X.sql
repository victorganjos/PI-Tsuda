CREATE	DATABASE PROJETO_X;
USE PROJETO_X;

CREATE TABLE projeto_x.DADOSUSUARIO(
ID INT AUTO_INCREMENT,
NOME varchar(50),
USERNAME varchar(50),
SENHA varchar(50),
CARGO varchar(50),
FILIAL varchar(50),
constraint pk_id primary key (id)
);
/**
